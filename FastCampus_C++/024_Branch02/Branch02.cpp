#include<iostream>

using namespace std;

int main()
{
    int num = 100;

    if (num % 2 == 0)
    {
        cout << "짝수 입니다." << endl;
    }
    else
    {
        cout << "홀수 입니다." << endl;
    }

    if (num > 100)
    {
        cout << "num은 100보다 크다." << endl;
    }
    else
    {
        cout << "num은 100보다 작다." << endl;
    }

    cout << "num: " << num << endl;

    return 0;
}

/* Result
짝수 입니다.
num은 100보다 작다.
num: 100
*/

